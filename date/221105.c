#include <stdio.h>
int main() {
	int a[100]= {0};
	int b,c = 0, t;
	scanf("%d", &b);
	for(int i = 0; i< b; i++){
		scanf("%d", &a[i]);
	}
	for(int i =0; i < b; i++){
		t = 0;
		for(int j = i; j < b; j++){
			if(a[i] > a[j]) t++;
		}
		if(t >= b - i - 1) c++;
	}
	printf("%d", c-1);
	return 0;
}
